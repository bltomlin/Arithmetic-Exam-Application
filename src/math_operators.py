# -*- coding: utf-8 -*-
"""
This module contains all mathematical operators that will be
utilized in this project (e.g add, subtract, multiply, etc.)
"""


def add(num1, num2):
    """
    Function that adds two numbers together.

    :param num1: the first addend
    :param num2: the second addend
    :return: the resultant sum
    """
    return num1 + num2


def subtract(num1, num2):
    """
    Function that takes the difference of two numbers.

    :param num1: the minuend
    :param num2: the subtrahend
    :return: the resultant difference
    """
    return num1 - num2


def multiply(num1, num2):
    """
    Function that multiplies two numbers.

    :param num1: the multiplier
    :param num2: the multiplicand
    :return: the resultant product
    """
    return num1 * num2
